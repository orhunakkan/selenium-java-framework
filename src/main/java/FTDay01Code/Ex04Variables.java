package FTDay01Code;

public class Ex04Variables {

    /*
     * Variables
     * 
     * Variables are used to store data values
     * 
     * In Java, there are different types of variables, for example:
     * String, int, double, char, boolean, etc...
     * 
     * Each variable has a specific type, which determines the size and value range of the variable
     * 
     * There are 2 types of variables:
     * 
     * 1. Local variables
     * 2. Instance variables
     * 
     * Local variables are declared inside a method, constructor or a block
     * 
     * Instance variables are declared inside a class, but outside a method, constructor or a block
     * Instance variables are also known as fields
     * 
     * Visibility:
     * 
     * 1. Public - the variable can be accessed from anywhere
     * 2. Private - the variable can only be accessed within the declared class
     * 3. Protected - the variable can be accessed within the declared class, and from within any subclass
     * 4. Default - the variable can only be accessed within the declared package
     * 
     * Note: Instance variables are visible for all methods, constructors and blocks in the class
     * 
     * Note: Local variables are only visible to the method, constructor or block in which they are declared
     * 
     */
}
